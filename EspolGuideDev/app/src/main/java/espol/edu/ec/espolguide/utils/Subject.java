public class Subject extends android.support.v7.widget.AppCompatButton {
    private String day;
    private String place;

    public Subject(Context ctx, String day, String place){
        super(ctx);
        this.day = day;
        this.place = place;
    }

    public String toString(){
        return (this.day +" | "+this.place);
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }


}

